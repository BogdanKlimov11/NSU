#!/bin/bash

# Проверяем количество аргументов и их тип
if [ "$#" -ne 2 ]; then
  echo "Ошибка: должно быть два аргумента."
  exit 1
fi

# Проверяем, являются ли оба аргумента числами
if ! [[ "$1" =~ ^-?[0-9]+$ ]] || ! [[ "$2" =~ ^-?[0-9]+$ ]]; then
  echo "Ошибка: оба аргумента должны быть числами."
  exit 1
fi

# Выводим сумму
sum=$(( $1 + $2 ))
echo "Сумма: $sum"
