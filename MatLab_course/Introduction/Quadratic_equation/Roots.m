function [x1, x2] = korni(a, b, c)
    if a ~= 0
        % Вычисление дискриминанта
        D = b^2 - 4*a*c;
        
        if D < 0
            % Если дискриминант отрицательный, то корней нет
            x1 = NaN;
            x2 = NaN;
            fprintf('Нет действительных корней.\n');
        else
            % Если дискриминант не отрицателен, вычисляем корни
            x1 = (-b - sqrt(D)) / (2*a);
            x2 = (-b + sqrt(D)) / (2*a);
            fprintf('Корни: %g и %g\n', x1, x2);
        end
    else
        % Если a = 0, у нас линейное уравнение bx + c = 0
        if b == 0
            if c == 0
                % Уравнение 0 = 0 имеет бесконечно много решений
                x1 = NaN;
                x2 = NaN;
                fprintf('Бесконечно много решений.\n');
            else
                % Уравнение c = 0 не имеет решений, если c != 0
                x1 = NaN;
                x2 = NaN;
                fprintf('Нет решений.\n');
            end
        else
            % Линейное уравнение bx + c = 0 имеет один корень
            x1 = -c / b;
            x2 = NaN;  % Второй корень не существует
            fprintf('Корень линейного уравнения: %g\n', x1);
        end
    end
end
